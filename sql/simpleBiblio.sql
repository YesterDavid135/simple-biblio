DROP TABLE IF EXISTS "tbl_mediaType";
CREATE TABLE "tbl_mediaType"
(
    "idMediaType" integer     NOT NULL,
    "mediaType"   varchar(20) NOT NULL,
    PRIMARY KEY ("idMediaType")
);



DROP TABLE IF EXISTS "tbl_location";
CREATE TABLE "tbl_location"
(
    "idLoc"    integer     NOT NULL,
    "ZIP"      varchar(4)  NOT NULL,
    "location" varchar(45) NOT NULL,
    PRIMARY KEY ("idLoc")
);



DROP TABLE IF EXISTS "tbl_users";
CREATE TABLE "tbl_users"
(
    "iduser"      serial       NOT NULL,
    "name"        varchar(45)  NOT NULL,
    "vname"       varchar(45)  NOT NULL,
    "yob"         year(4)     DEFAULT NULL,
    "street"      varchar(45) DEFAULT NULL,
    "fk_location" integer     DEFAULT NULL,
    "mail"        varchar(45) DEFAULT NULL,
    "password"    varchar(255) NOT NULL,
    "username"    varchar(20)  NOT NULL,
    PRIMARY KEY ("iduser"),
    UNIQUE KEY "tbl_users_username_uindex" ("username"),
    KEY "location" ("fk_location"),
    CONSTRAINT "location" FOREIGN KEY ("fk_location") REFERENCES "tbl_location" ("idLoc")
);



DROP TABLE IF EXISTS "tbl_items";
CREATE TABLE "tbl_items"
(
    "idMedia"      serial primary key,
    "title"        varchar(45) NOT NULL,
    "ageRating"    integer     NOT NULL,
    "autor"        varchar(45) NOT NULL,
    "ISBN"         bigint      NOT NULL DEFAULT,
    "shelf"        integer     NOT NULL,
    "fk_mediaType" integer     NOT NULL,
    CONSTRAINT "tbl_inventory_tbl_mediaType_idMediaType_fk" FOREIGN KEY ("fk_mediaType") REFERENCES "tbl_mediaType" ("idMediaType")
);



DROP TABLE IF EXISTS "tbl_borrowed";
CREATE TABLE "tbl_borrowed"
(
    "idBorrow"  serial primary key,
    "fk_idItem" integer   NOT NULL,
    "fk_idUser" integer   NOT NULL,
    "timestamp" timestamp NOT NULL DEFAULT NOW(),
    "returned"  smallint  NOT NULL DEFAULT 0,
    CONSTRAINT "tbl_borrowed_tbl_inventory_idMedia_fk" FOREIGN KEY ("fk_idItem") REFERENCES "tbl_items" ("idMedia"),
    CONSTRAINT "tbl_borrowed_tbl_users_iduser_fk" FOREIGN KEY ("fk_idUser") REFERENCES "tbl_users" ("iduser")
);



